    /**
     * @param activity
     * @param res
     * @return
     */
    public static TextView[] getTextViewList(final Activity activity, final int[] res) {
        TextView[] tv = new TextView[res.length];
        for (int i = 0; i < tv.length; i++) {
            tv[i] = ((TextView) activity.findViewById(res[i]));
        }
        return tv;
    }

    /**
     * @param activity
     * @return
     */
    public static TextView[][] getPlayerScoreTextViewList(final Activity activity) {

        TextView[] scoreTv1 = SVRes.getTextViewList(activity, SVRes.PERSON_1_AREA);
        TextView[] scoreTv2 = SVRes.getTextViewList(activity, SVRes.PERSON_2_AREA);
        TextView[] scoreTv3 = SVRes.getTextViewList(activity, SVRes.PERSON_3_AREA);
        TextView[] scoreTv4 = SVRes.getTextViewList(activity, SVRes.PERSON_4_AREA);
        TextView[][] playerScores = { scoreTv1, scoreTv2, scoreTv3, scoreTv4 };

        return playerScores;
    }
